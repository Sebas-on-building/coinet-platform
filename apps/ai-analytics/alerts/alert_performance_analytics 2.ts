  /**
   * Calculate trend using linear regression
   */
  private calculateTrend(returns: number[]): { slope: number; direction: 'improving' | 'stable' | 'degrading' | 'volatile'; rSquared: number } {
    const n = returns.length;
    const x = Array.from({ length: n }, (_, i) => i);
    const y = returns;

    const sumX = x.reduce((a, b) => a + b, 0);
    const sumY = y.reduce((a, b) => a + b, 0);
    const sumXY = x.reduce((acc, xi, i) => acc + xi * y[i], 0);
    const sumXX = x.reduce((acc, xi) => acc + xi * xi, 0);

    const slope = (n * sumXY - sumX * sumY) / (n * sumXX - sumX * sumX);

    // Calculate R-squared
    const meanY = sumY / n;
    const totalSumSquares = y.reduce((acc, yi) => acc + Math.pow(yi - meanY, 2), 0);
    const residualSumSquares = y.reduce((acc, yi, i) => {
      const predicted = slope * x[i] + (sumY - slope * sumX) / n;
      return acc + Math.pow(yi - predicted, 2);
    }, 0);

    const rSquared = totalSumSquares > 0 ? 1 - (residualSumSquares / totalSumSquares) : 0;

    let direction: 'improving' | 'stable' | 'degrading' | 'volatile' = 'stable';
    if (Math.abs(slope) > 0.01) {
      direction = slope > 0 ? 'improving' : 'degrading';
    } else if (rSquared < 0.3) {
      direction = 'volatile';
    }

    return { slope, direction, rSquared };
  }

