  /**
   * Calculate consistency score based on outcome patterns
   */
  private calculateConsistencyScore(outcomes: any[]): number {
    if (outcomes.length < 3) return 0.5; // Neutral score for small sample sizes

    // Calculate the entropy of outcomes (lower entropy = more consistent)
    const outcomeCounts = outcomes.reduce((counts, outcome) => {
      counts[outcome.outcome] = (counts[outcome.outcome] || 0) + 1;
      return counts;
    }, {} as Record<string, number>);

    const total = outcomes.length;
    let entropy = 0;

    for (const count of Object.values(outcomeCounts)) {
      const p = count as number / total;
      if (p > 0) {
        entropy -= p * Math.log2(p);
      }
    }

    // Normalize entropy (max entropy for 4 outcomes is ~2 bits)
    const normalizedEntropy = entropy / 2;
    return 1 - normalizedEntropy; // Higher consistency = lower entropy
  }

