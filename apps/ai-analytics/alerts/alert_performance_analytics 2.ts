  /**
   * Calculate advanced risk metrics
   */
  private calculateAdvancedRiskMetrics(outcomes: any[]): AlertPerformanceMetrics['riskMetrics'] {
    const returns = outcomes
      .filter(o => o.profit_loss && o.entry_price)
      .map(o => o.profit_loss! / o.entry_price!);

    if (returns.length === 0) {
      return {
        valueAtRisk: { daily: 0, weekly: 0, monthly: 0 },
        expectedShortfall: { daily: 0, weekly: 0, monthly: 0 },
        tailRisk: 0,
        downsideDeviation: 0,
        upsideDeviation: 0,
        gainLossRatio: 0,
        sterlingRatio: 0,
        burkeRatio: 0
      };
    }

    // Value at Risk (95% confidence)
    const sortedReturns = [...returns].sort((a, b) => a - b);
    const var95Index = Math.floor(returns.length * 0.05);
    const var95 = Math.abs(sortedReturns[var95Index]);

    // Expected Shortfall (Conditional VaR)
    const tailReturns = sortedReturns.slice(0, var95Index);
    const expectedShortfall = tailReturns.length > 0 ?
      tailReturns.reduce((a, b) => a + b, 0) / tailReturns.length : 0;

    // Tail Risk
    const tailRisk = this.calculateTailRisk(returns);

    // Downside Deviation
    const downsideDeviation = this.calculateDownsideDeviation(returns);

    // Upside Deviation
    const upsideDeviation = this.calculateUpsideDeviation(returns);

    // Gain/Loss Ratio
    const gains = returns.filter(r => r > 0).reduce((a, b) => a + b, 0);
    const losses = Math.abs(returns.filter(r => r < 0).reduce((a, b) => a + b, 0));
    const gainLossRatio = losses > 0 ? gains / losses : 0;

    // Sterling Ratio: Average annual return / Maximum drawdown
    const avgAnnualReturn = returns.reduce((a, b) => a + b, 0) / returns.length * 252;
    const maxDrawdown = this.calculateMaxDrawdown(outcomes);
    const sterlingRatio = maxDrawdown > 0 ? Math.abs(avgAnnualReturn) / maxDrawdown : 0;

    // Burke Ratio
    const burkeRatio = this.calculateBurkeRatio(returns);

    return {
      valueAtRisk: {
        daily: var95,
        weekly: var95 * Math.sqrt(7),
        monthly: var95 * Math.sqrt(30)
      },
      expectedShortfall: {
        daily: expectedShortfall,
        weekly: expectedShortfall * Math.sqrt(7),
        monthly: expectedShortfall * Math.sqrt(30)
      },
      tailRisk,
      downsideDeviation,
      upsideDeviation,
      gainLossRatio,
      sterlingRatio,
      burkeRatio
    };
  }

  /**
   * Calculate tail risk
   */
  private calculateTailRisk(returns: number[]): number {
    const sortedReturns = [...returns].sort((a, b) => a - b);
    const worstReturns = sortedReturns.slice(0, Math.floor(returns.length * 0.05));
    return worstReturns.length > 0 ?
      worstReturns.reduce((a, b) => a + b, 0) / worstReturns.length : 0;
  }

  /**
   * Calculate downside deviation
   */
  private calculateDownsideDeviation(returns: number[]): number {
    const mean = returns.reduce((a, b) => a + b, 0) / returns.length;
    const downsideReturns = returns.filter(r => r < mean).map(r => Math.pow(r - mean, 2));
    const downsideVariance = downsideReturns.length > 0 ?
      downsideReturns.reduce((a, b) => a + b, 0) / downsideReturns.length : 0;
    return Math.sqrt(downsideVariance);
  }

  /**
   * Calculate upside deviation
   */
  private calculateUpsideDeviation(returns: number[]): number {
    const mean = returns.reduce((a, b) => a + b, 0) / returns.length;
    const upsideReturns = returns.filter(r => r > mean).map(r => Math.pow(r - mean, 2));
    const upsideVariance = upsideReturns.length > 0 ?
      upsideReturns.reduce((a, b) => a + b, 0) / upsideReturns.length : 0;
    return Math.sqrt(upsideVariance);
  }

  /**
   * Calculate Burke Ratio
   */
  private calculateBurkeRatio(returns: number[]): number {
    const downsideDeviation = this.calculateDownsideDeviation(returns);
    const mean = returns.reduce((a, b) => a + b, 0) / returns.length;
    return downsideDeviation > 0 ? mean / downsideDeviation : 0;
  }

  /**
   * Calculate Sharpe ratio for risk-adjusted returns
   */
  private calculateSharpeRatio(returns: number[]): number {
    if (returns.length === 0) return 0;

    const avgReturn = returns.reduce((a, b) => a + b, 0) / returns.length;
    const variance = returns.reduce((acc, ret) => acc + Math.pow(ret - avgReturn, 2), 0) / returns.length;
    const stdDev = Math.sqrt(variance);

    // Assuming 2% annual risk-free rate, annualized
    const riskFreeRate = 0.02 / 252; // Daily risk-free rate

    return stdDev === 0 ? 0 : (avgReturn - riskFreeRate) / stdDev;
  }

  /**
   * Calculate Sortino ratio for downside risk-adjusted returns
   */
  private calculateSortinoRatio(returns: number[]): number {
    if (returns.length === 0) return 0;

    const avgReturn = returns.reduce((a, b) => a + b, 0) / returns.length;
    const downsideReturns = returns.filter(r => r < 0);
    const downsideVariance = downsideReturns.length > 0
      ? downsideReturns.reduce((acc, ret) => acc + Math.pow(ret, 2), 0) / downsideReturns.length
      : 0;
    const downsideStdDev = Math.sqrt(downsideVariance);

    const riskFreeRate = 0.02 / 252;

    return downsideStdDev === 0 ? 0 : (avgReturn - riskFreeRate) / downsideStdDev;
  }

  /**
   * Calculate maximum drawdown
   */
  private calculateMaxDrawdown(outcomes: any[]): number {
    if (outcomes.length === 0) return 0;

    let peak = 0;
    let maxDrawdown = 0;
    let runningTotal = 0;

    for (const outcome of outcomes) {
      if (outcome.profit_loss) {
        runningTotal += outcome.profit_loss;
        if (runningTotal > peak) {
          peak = runningTotal;
        }
        const drawdown = peak - runningTotal;
        if (drawdown > maxDrawdown) {
          maxDrawdown = drawdown;
        }
      }
    }

    return maxDrawdown;
  }

