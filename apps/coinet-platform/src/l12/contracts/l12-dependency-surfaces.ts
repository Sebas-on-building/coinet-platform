/**
 * L12.1 — Dependency Surface Contract
 *
 * §12.1.5 / §12.1.9 — Hard constitutional dependency law for the
 * Scenario Engine. Every registered dependency surface declares its
 * source layer, authority class, freshness, replay legality, and
 * posture awareness (regime, sequence, hypothesis, restriction).
 *
 * §12.1.5.6–§12.1.5.9 — L7, L8, L9, L10 and L11 surfaces are stable
 * handoff only. Internal runtime state of those layers is forbidden.
 * §12.1.5.9 — L11 score consumption requires the full score CONTEXT
 * BUNDLE; naked score values are illegal.
 */

