export interface L12DependencySurfaceDescriptor {
  readonly surfaceId: string;
  readonly layer: L12DependencyLayer;
  readonly surfaceClass: L12DependencySurfaceClass;
  readonly authorityClass:
    | 'CANONICAL'
    | 'DERIVED'
    | 'CONTEXTUAL'
    | 'EVIDENCE'
    | 'STABLE_HANDOFF';
  readonly freshnessExpectation:
    | 'REAL_TIME'
    | 'NEAR_REAL_TIME'
    | 'PERIODIC'
    | 'ON_DEMAND';
  readonly replayCompatible: boolean;
  readonly stableHandoffRequired: boolean;
  readonly restrictionAware: boolean;
  readonly contradictionAware: boolean;
  readonly regimePostureAware: boolean;
  readonly sequencePostureAware: boolean;
  readonly hypothesisPostureAware: boolean;
  readonly scoreContextRequired: boolean;
  readonly usableFor: readonly L12DependencyUsability[];
  readonly legalConsumptionContexts: readonly L12CapabilityContext[];
  readonly forbiddenUseCases: readonly L12ForbiddenAction[];
  readonly required: boolean;
  readonly policyVersion: string;
  readonly description: string;
}

