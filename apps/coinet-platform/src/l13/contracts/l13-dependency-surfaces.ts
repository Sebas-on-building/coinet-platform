/**
 * L13.1 — Dependency Surface Contract
 *
 * §13.1.3 / §13.1.7 — Hard constitutional dependency law for the
 * AI Judgment & Explanation Layer. Every registered dependency
 * surface declares its source layer (L3–L12), surface class, and the
 * disclosure / posture-awareness it requires.
 *
 * Layer 13 may consume only registered governed handoff surfaces
 * from L3–L12. L14+ consumption is forbidden. Raw lower-layer rebuild
 * surfaces are forbidden. L11 score requires the full
 * attribution / missing-data / drift / restriction context bundle.
 * L12 scenario requires triggers / invalidations / path confidence /
 * restrictions context.
 */

import {
  L13AllowedCapability,
  L13DependencyLayer,
  L13DependencySurfaceClass,
} from './l13-constitutional-types';

export interface L13DependencySurfaceDescriptor {
  readonly surfaceId: string;
  readonly sourceLayer: L13DependencyLayer;
  readonly surfaceClass: L13DependencySurfaceClass;

  readonly stableHandoffRequired: boolean;

  readonly evidenceRequired: boolean;
  readonly lineageRequired: boolean;
  readonly restrictionRequired: boolean;
  readonly confidenceRequired: boolean;

  readonly contradictionAware: boolean;
  readonly regimeAware: boolean;
  readonly sequenceAware: boolean;
  readonly hypothesisAware: boolean;
  readonly scoreContextAware: boolean;
  readonly scenarioAware: boolean;

  /** §13.1.3 — Raw rebuild is always forbidden for L13 surfaces. */
  readonly rawRebuildProhibited: true;

  /** §13.1.7 / §13.1.9 — Capabilities allowed to use this surface. */
  readonly allowedL13Uses: readonly L13AllowedCapability[];

  readonly required: boolean;
  readonly policyVersion: string;
  readonly description: string;
}

