export function getL13DependencySurface(
  surfaceId: string,
): L13DependencySurfaceDescriptor | undefined {
  return L13_DEPENDENCY_SURFACES.find(s => s.surfaceId === surfaceId);
}

export function isL13RegisteredDependency(surfaceId: string): boolean {
  return L13_DEPENDENCY_SURFACES.some(s => s.surfaceId === surfaceId);
}

export function getL13SurfacesForLayer(
  layer: L13DependencyLayer,
): readonly L13DependencySurfaceDescriptor[] {
  return L13_DEPENDENCY_SURFACES.filter(s => s.sourceLayer === layer);
}

export function getL13RequiredDependencySurfaces():
  readonly L13DependencySurfaceDescriptor[] {
  return L13_DEPENDENCY_SURFACES.filter(s => s.required);
}

export function getL13ContradictionAwareSurfaces():
  readonly L13DependencySurfaceDescriptor[] {
  return L13_DEPENDENCY_SURFACES.filter(s => s.contradictionAware);
}

export function getL13ScoreContextSurfaces():
  readonly L13DependencySurfaceDescriptor[] {
  return L13_DEPENDENCY_SURFACES.filter(s => s.scoreContextAware);
}

export function getL13ScenarioSurfaces():
  readonly L13DependencySurfaceDescriptor[] {
  return L13_DEPENDENCY_SURFACES.filter(s => s.scenarioAware);
}

export function getL13RestrictionAwareSurfaces():
  readonly L13DependencySurfaceDescriptor[] {
  return L13_DEPENDENCY_SURFACES.filter(s => s.restrictionRequired);
}

export function isL13UseAllowed(
  surfaceId: string,
  capability: L13AllowedCapability,
): boolean {
  const s = getL13DependencySurface(surfaceId);
  if (!s) return false;
  return s.allowedL13Uses.includes(capability);
}
